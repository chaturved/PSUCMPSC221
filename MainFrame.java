    private boolean reserveRoom(String facultyselected,Date dateselected,int seatsentered,int option){
        ArrayList<RoomEntry> roomsreservedbydate=this.reservationqueries.getRoomsReservedByDate(dateselected);
        ArrayList<RoomEntry> allPossibleRooms=this.roomqueries.getAllPossibleRooms();
        
        for(int i=allPossibleRooms.size()-1;i>=0;i--){      
            if(seatsentered > allPossibleRooms.get(i).getSeats()){
                allPossibleRooms.remove(i);
            }
        }
        
        for(int i=roomsreservedbydate.size()-1;i>=0;i--){
            for(int j=allPossibleRooms.size()-1;j>=0;j--)
            if(allPossibleRooms.get(j).getName().equals(roomsreservedbydate.get(i).getName())){
                allPossibleRooms.remove(j);
            }
        }
        
        if(allPossibleRooms.isEmpty()){
            if(option==0){
                WaitlistEntry waitlistentry=new WaitlistEntry(facultyselected,dateselected,seatsentered,new Timestamp(Calendar.getInstance().getTime().getTime()));
                this.waitlistqueries.addWaitlistEntry(waitlistentry);
            }
            
                    
            return false;
        }
        
        int minseats=allPossibleRooms.get(0).getSeats();
        this.minroom=allPossibleRooms.get(0).getName();
        for(int i=0;i<allPossibleRooms.size();i++){
            if(allPossibleRooms.get(i).getSeats() < minseats){
                minseats=allPossibleRooms.get(i).getSeats();
                this.minroom=allPossibleRooms.get(i).getName();
            }
        }
        
        ReservationEntry reservationentry=new ReservationEntry(facultyselected,this.minroom,dateselected,minseats,new Timestamp(Calendar.getInstance().getTime().getTime()));
        this.reservationqueries.addReservationEntry(reservationentry);
        
        return true;
     
    }
        

