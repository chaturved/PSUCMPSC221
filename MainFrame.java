    private void roomSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_roomSubmitButtonActionPerformed
        // TODO add your handling code here:
        String roomName=this.roomNameTextField.getText();
        int roomSeats=Integer.parseInt(this.roomSeatsTextField.getText());
        String entries="Room added!\n";

        RoomEntry roomentry=new RoomEntry(roomName,roomSeats);
        this.roomqueries.addRoom(roomentry);

        ArrayList <WaitlistEntry> waitlist= this.waitlistqueries.getWaitlistDTR();
        for(int i=0;i<waitlist.size();i++){
            if(this.reserveRoom(waitlist.get(i).getFaculty(), waitlist.get(i).getDate(), waitlist.get(i).getSeats(), 1)){
                entries=entries+waitlist.get(i).getFaculty()+" is removed from waitlist and the reservation made in Room "+this.minroom+" on "+waitlist.get(i).getDate().toString()+" for "+waitlist.get(i).getSeats()+" seats"+"\n";
                this.waitlistqueries.deleteWaitlistEntry(waitlist.get(i));
            }
        }

        this.roomStatusArea.setText(entries);

    }//GEN-LAST:event_roomSubmitButtonActionPerformed

