    private void dropRoomSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropRoomSubmitButtonActionPerformed
        // TODO add your handling code here:
        String roomName=this.dropRoomComboBox.getSelectedItem().toString();
        this.roomqueries.dropRoom(roomName);
        
        ArrayList<ReservationEntry> reservations=this.reservationqueries.getReservationsByRoom(roomName);
        ArrayList<String> faculties=new ArrayList<>();
        
        for(int i=0;i<reservations.size();i++){
            WaitlistEntry waitlistentry=new WaitlistEntry(reservations.get(i).getFaculty(),reservations.get(i).getDate(),reservations.get(i).getSeats(),reservations.get(i).getTimestamp());
            this.waitlistqueries.addWaitlistEntry(waitlistentry);
            
            faculties.add(reservations.get(i).getFaculty());
            this.reservationqueries.deleteReservation(reservations.get(i));
        }
        
        
        ArrayList<WaitlistEntry> waitlist=this.waitlistqueries.getWaitlistDTR();
        String entries="";
       
        for(int i=0;i<waitlist.size();i++){
            if(this.reserveRoom(waitlist.get(i).getFaculty(), waitlist.get(i).getDate(), waitlist.get(i).getSeats(), 1)){
                if(faculties.contains(waitlist.get(i).getFaculty())){
                    entries=entries+waitlist.get(i).getFaculty()+" was reserved a New Room in "+this.minroom+" on "+waitlist.get(i).getDate().toString()+"\n";
                }
                else{
                    
                    entries=entries+waitlist.get(i).getFaculty()+" was removed from the waitlist and was reserved a Room in "+this.minroom+" on "+waitlist.get(i).getDate().toString()+"\n";
                }
                
                this.waitlistqueries.deleteWaitlistEntry(new WaitlistEntry(waitlist.get(i).getFaculty(),waitlist.get(i).getDate()));
                
            }
            else{
            
                entries=entries+waitlist.get(i).getFaculty()+" was added to the waitlist for "+waitlist.get(i).getSeats()+" seats on "+waitlist.get(i).getDate().toString()+"\n";
            }
        }
        
        this.dropRoomStatusArea.setText(entries);
        this.setComboBoxModels();
    }//GEN-LAST:event_dropRoomSubmitButtonActionPerformed

