    private void cancelSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelSubmitButtonActionPerformed
        // TODO add your handling code here:
        String faculty=this.cancelFacultyComboBox.getSelectedItem().toString();
        Date date=(Date) this.cancelDateComboBox.getSelectedItem();
        
        int cntr=this.reservationqueries.deleteReservation(new ReservationEntry(faculty,date));
        int cntw=this.waitlistqueries.deleteWaitlistEntry(new WaitlistEntry(faculty,date));
        
        if(cntr==0 && cntw==0){
            this.cancelStatusArea.setText("No reservation found!");
        }
        else{
        
            String entries="Reservation Cancelled\n";
            ArrayList <WaitlistEntry> waitlist=this.waitlistqueries.getWaitlistByDateR(date);
            
            for(int i=0;i<waitlist.size();i++){
                if(this.reserveRoom(waitlist.get(i).getFaculty(), waitlist.get(i).getDate(), waitlist.get(i).getSeats(),0)){
                    this.waitlistqueries.deleteWaitlistEntry(waitlist.get(i));
                    entries=entries+waitlist.get(i).getFaculty()+" was removed from waitlist and added to the reservations in Room "+this.minroom+"\n";
                }
            }
            
            this.cancelStatusArea.setText(entries);
        }
        
    }//GEN-LAST:event_cancelSubmitButtonActionPerformed

